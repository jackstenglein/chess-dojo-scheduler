func getCohort(ratingSystem RatingSystem, currentRating int) DojoCohort {
	boundaries := ratingBoundaries[ratingSystem]
	if boundaries == nil {
		return NoCohort
	}

	for i := 0; i < len(boundaries); i++ {
		if currentRating < boundaries[i] {
			return Cohorts[i]
		}
	}

	return "2400+"
}
