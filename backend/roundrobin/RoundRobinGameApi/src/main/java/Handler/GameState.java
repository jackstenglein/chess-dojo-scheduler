package Handler;

/**
 * The enum Game state.
 */
public enum GameState {

    /**
     * Player one won game state.
     */
    PLAYER_ONE_WON,

    /**
     * Player two won game state.
     */
    PLAYER_TWO_WON,

    /**
     * Player one lost game state.
     */
    PLAYER_ONE_LOST,

    /**
     * Player two lost game state.
     */
    PLAYER_TWO_LOST,

    /**
     * Draw game state.
     */
    DRAW












}

